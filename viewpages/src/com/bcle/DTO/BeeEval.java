package com.bcle.DTO;

public class BeeEval
{
	private String beeEvalId, reqId, content, bdate, nickname, clubmemId;
	private int score;
	public String getBeeEvalId()
	{
		return beeEvalId;
	}
	public void setBeeEvalId(String beeEvalId)
	{
		this.beeEvalId = beeEvalId;
	}
	public String getReqId()
	{
		return reqId;
	}
	public void setReqId(String reqId)
	{
		this.reqId = reqId;
	}
	public String getContent()
	{
		return content;
	}
	public void setContent(String content)
	{
		this.content = content;
	}
	public String getBdate()
	{
		return bdate;
	}
	public void setBdate(String bdate)
	{
		this.bdate = bdate;
	}
	public int getScore()
	{
		return score;
	}
	public void setScore(int score)
	{
		this.score = score;
	}
	public String getNickname()
	{
		return nickname;
	}
	public void setNickname(String nickname)
	{
		this.nickname = nickname;
	}
	public String getClubmemId()
	{
		return clubmemId;
	}
	public void setClubmemId(String clubmemId)
	{
		this.clubmemId = clubmemId;
	}
	
	
}
